    private void BtnEqualsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEqualsActionPerformed
        calcEqualsPressed = true;
        if(op == ' ')
            return;
        String result = calcResultLabel.getText();
        if(result.endsWith("."))
            result = result.substring(0, result.length() - 1);
        calcVal2 = Double.parseDouble(result);
        double resultVal = 0;
        if(calcVal2 == 0 && op == '/'){
            JOptionPane.showMessageDialog(null, "Division by zero error", "Error", JOptionPane.ERROR_MESSAGE);
            op = ' ';
            BtnCE.doClick();
            return;
        }
        switch(op){
            case '+': resultVal = calcVal1 + calcVal2;
                break;
            case '-': resultVal = calcVal1 - calcVal2;
                break;
            case '*': resultVal = calcVal1 * calcVal2;
                break;
            case '/': resultVal = calcVal1 / calcVal2;
                break;    
        }
        calcResultLabel.setText("" + resultVal);
        op = ' ';
    }//GEN-LAST:event_BtnEqualsActionPerformed

