    private void DigitPressed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DigitPressed
        int digit = Integer.parseInt(evt.getActionCommand());
        String val = calcResultLabel.getText();
        if((val.equals("0") || opPressed || calcEqualsPressed) && !val.startsWith("0.")){
            opPressed = false;
            calcEqualsPressed = false;
            calcResultLabel.setText("" + digit);
            return;  
        }
        else if(opPressed && val.startsWith("0.")){
            opPressed = false;
            calcEqualsPressed = false;
            calcResultLabel.setText("" + digit);
            return;
        }
            
        calcResultLabel.setText(val + digit);
    }//GEN-LAST:event_DigitPressed

