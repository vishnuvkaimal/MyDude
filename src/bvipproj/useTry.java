    private void convertBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_convertBtnActionPerformed
        double valToConvert = 0;
        try{
            valToConvert = Double.parseDouble(conversionValTF.getText());
            if(valToConvert < 0 && conversionTypeCB.getSelectedIndex() != 3)
                throw new NumberFormatException();
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Please enter a valid conversion quantity.");
            return;
        }
    
        int ind1=conversionFromCB.getSelectedIndex();
        int ind2=conversionToCB.getSelectedIndex();
        int type = conversionTypeCB.getSelectedIndex();
        double val=Double.parseDouble(conversionValTF.getText());
        double conF[]={};
        if(type==0)
        { 
            conF=new double[]{1.0/60,1.0/60,1.0/24};
        }
        else if(type==1)
        {
            conF=new double[]{1.0/100,100/2.54,1.0/12};
        }
        else if(type==2)
        {
            conF=new double[]{1.0/1000,2.205,0};
        }
        else if(type == 3){
            if(ind1<ind2)
            {
                for(int i=ind1+1;i<=ind2;i++)
                {
                    if(i == 1)
                        val = celsiusToFah(val);
                    else if (i == 2)
                        val = fahToKelvin(val);
                }
            }
            else if(ind1>ind2)
            {
                for(int i=ind1+1;i>=ind2;i--)
                {
                    if(i == 3)
                        val = kelvinToFah(val);
                    else if (i == 2)
                        val = fahToCelsius(val);
                }
            }
            convertedValLabel.setText("" + val);
            return;
        }
        if(ind1<ind2)
        {
            for(int i=ind1;i<ind2;i++)
            {
                double x=conF[i];
                val=val*x;
            }
        }
        else if(ind1>ind2)
        {
            for(int i=ind1-1;i>=ind2;i--)
            {
                double x=conF[i];
                val=val*(1/x);
            }
        }
        convertedValLabel.setText("" + val);
    }//GEN-LAST:event_convertBtnActionPerformed
    private static double celsiusToKelvin(double temp){
        return temp + 273;
    }
    private static double kelvinToCelsius(double temp){
        return temp - 273;
    }
    private static double fahToCelsius(double temp){
        return 5 * (temp - 32) / 9;
    }
    private static double celsiusToFah(double temp){
        return (9 * temp /5) + 32;
    }
    private static double fahToKelvin(double temp){
        return celsiusToKelvin(fahToCelsius(temp));
    }
    private static double kelvinToFah(double temp){
        return celsiusToFah(kelvinToCelsius(temp));
    }
